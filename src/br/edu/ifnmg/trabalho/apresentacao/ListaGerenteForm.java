    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Usuario usuario = new Usuario();
        usuario.setId(Integer.parseInt(txtId.getText()));
        usuario.setNome(txtNome.getText());
        usuario.setEmail(txtEmail.getText());
        UsuarioBO usuarioBO = new UsuarioBO();
        String senha = txtSenha.getText().trim();
        if (!senha.isEmpty()) {
            usuario.setSenha(usuarioBO.criptografar(senha));
        } else {
            usuario.setSenha("");
        }
        usuario.setCargo("GERENTE");
        try {
            usuario.setDepartamento((Departamento) this.cmbDepartamento.getSelectedItem());
        } catch (Exception e) {
            usuario.setDepartamento(null);
        }
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        try {
            usuarioDAO.editar(usuario);
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
        ConfigurarTblGerente();
        ConfigurarCmbDepartamento();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void tblGerenteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGerenteMouseClicked
        int cod = tblGerente.getSelectedRow();
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        txtId.setText(Integer.toString(usuarioDAO.listaGerente().get(cod).getId()));
        txtNome.setText(usuarioDAO.listaGerente().get(cod).getNome());
        txtNome.setEnabled(true);
        txtEmail.setText(usuarioDAO.listaGerente().get(cod).getEmail());
        txtEmail.setEnabled(true);
        txtSenha.setEnabled(true);
        cmbDepartamento.setEnabled(true);
        btnEditar.setEnabled(true);
        btnDemitir.setEnabled(true);
    }//GEN-LAST:event_tblGerenteMouseClicked

