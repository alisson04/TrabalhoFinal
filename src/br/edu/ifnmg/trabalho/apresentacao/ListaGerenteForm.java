    private void ConfigurarCmbDepartamento() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) this.cmbDepartamento.getModel();
        model.removeAllElements();
        model.addElement("Selecione...");
        DepartamentoDAO departamentoDAO = new DepartamentoDAO();
        for (Departamento departamento : departamentoDAO.listaDepartamento()) {
            model.addElement(departamento);
        }
    }

    private void ConfigurarTblGerente() {
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        TableGerenteModel model = new TableGerenteModel(usuarioDAO.listaGerente());
        tblGerente.setModel(model);
    }

    private class TableGerenteModel extends AbstractTableModel {

        private List<Usuario> lista;

        public TableGerenteModel(List<Usuario> lista) {
            this.lista = lista;
        }

        @Override
        public int getRowCount() {
            return lista.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Usuario usuario = lista.get(rowIndex);
            if (columnIndex == 0) {
                return usuario.getNome();
            } else if (columnIndex == 1) {
                return usuario.getEmail();
            } else if (columnIndex == 2) {
                return usuario.getCargo();
            } else if (columnIndex == 3) {
                return usuario.getDepartamento().getNome();
            }
            return null;
        }

        @Override
        public String getColumnName(int column) {
            if (column == 0) {
                return "Nome";
            } else if (column == 1) {
                return "Email";
            } else if (column == 2) {
                return "Cargo";
            } else if (column == 3) {
                return "Departamento";
            }
            return null;
        }

    }
