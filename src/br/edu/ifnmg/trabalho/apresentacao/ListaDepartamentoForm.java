    private void tblDepartamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDepartamentoMouseClicked
        int cod = tblDepartamento.getSelectedRow();
        DepartamentoDAO departamentoDAO = new DepartamentoDAO();
        txtId.setText(Integer.toString(departamentoDAO.listaDepartamento().get(cod).getId()));
        txtCodigo.setText(departamentoDAO.listaDepartamento().get(cod).getCodigo());
        txtNome.setText(departamentoDAO.listaDepartamento().get(cod).getNome());
        txtNome.setEnabled(true);
        txtCodigo.setEnabled(true);
        btnEditar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_tblDepartamentoMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String nome = txtNome.getText().trim();
        String codigo = txtCodigo.getText().trim();
        if (!nome.isEmpty() && !codigo.isEmpty()) {
            if (codigo.length() == 3) {
                int conf = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja alterar?", "Confirmação", JOptionPane.YES_NO_OPTION);
                if (conf == JOptionPane.YES_OPTION) {
                    try {
                        DepartamentoDAO departamentoDAO = new DepartamentoDAO();
                        Departamento departamento = new Departamento();
                        departamento.setId(Integer.parseInt(txtId.getText()));
                        departamento.setCodigo(codigo);
                        departamento.setNome(nome);
                        departamentoDAO.editar(departamento);
                        configurarTableDepartamento();
                    } catch (Exception e) {
                        throw new RuntimeException(e);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Codigo deve possuir somente 3 caracter!", "Erro", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Erro", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int conf = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (conf == JOptionPane.YES_OPTION) {
            try {
                DepartamentoDAO departamentoDAO = new DepartamentoDAO();
                departamentoDAO.excluir(Integer.parseInt(txtId.getText()));
                JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!", "Erro", JOptionPane.WARNING_MESSAGE);
                configurarTableDepartamento();
                txtId.setText("");
                txtId.setEnabled(false);
                txtCodigo.setText("");
                txtCodigo.setEnabled(false);
                txtNome.setText("");
                txtNome.setEnabled(false);
                btnEditar.setEnabled(false);
                btnExcluir.setEnabled(false);
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


