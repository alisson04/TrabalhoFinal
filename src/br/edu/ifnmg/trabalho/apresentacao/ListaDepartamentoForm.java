    private void configurarTableDepartamento() {
        DepartamentoDAO departamentoDAO = new DepartamentoDAO();
        TableDepartamentoModel model = new TableDepartamentoModel(departamentoDAO.listaDepartamento());
        tblDepartamento.setModel(model);
    }

    private class TableDepartamentoModel extends AbstractTableModel {

        private List<Departamento> lista;

        public TableDepartamentoModel(List<Departamento> lista) {
            this.lista = lista;
        }

        @Override
        public int getRowCount() {
            return lista.size();
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Departamento departamento = lista.get(rowIndex);
            if (columnIndex == 0) {
                return departamento.getCodigo();
            } else if (columnIndex == 1) {
                return departamento.getNome();
            }
            return null;
        }

        @Override
        public String getColumnName(int column) {
            if (column == 0) {
                return "Codigo";
            } else if (column == 1) {
                return "Nome";
            }
            return null;
        }

    }
